bot.action(/info_(.+)/, async (ctx) => {
    const doctorKey = ctx.match[1];
    const data = doctorData[doctorKey];
    
    if (data) {
        await ctx.answerCbQuery();
        await ctx.editMessageText(data.text, {
            parse_mode: 'Markdown',
            ...Markup.inlineKeyboard([
                [Markup.button.url('Написать врачу лично 💬', data.link)],
                [Markup.button.callback('⬅️ Вернуться к списку', 'back_to_list')]
            ])
        });
    }
});

bot.action('back_to_list', async (ctx) => {
    await ctx.answerCbQuery();
    await ctx.editMessageText('Выберите нужного специалиста для получения детальной информации:', doctorButtons);
});

bot.on('text', (ctx) => {
  ctx.reply('Ваше сообщение получено, ожидайте ответа врача.');
});

